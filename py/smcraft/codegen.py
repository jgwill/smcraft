def generate_python(
    input_path: str | Path,
    output_path: Optional[str | Path] = None,
) -> str:
    """
    Generate Python state machine code from a definition file.

    Args:
        input_path: Path to .smdf.json or .smdf.xml file
        output_path: Optional output file path. If None, returns code as string.

    Returns:
        Generated Python source code
    """
    parser = StateMachineParser()
    model = parser.parse_file(input_path)
    errors = parser.validate(model)
    if errors:
        error_msgs = "\n".join(f"  [{e.rule_id}] {e.message}" for e in errors)
        raise ValueError(f"Validation errors:\n{error_msgs}")

    generator = PythonCodeGenerator(model)
    code = generator.generate()

    if output_path:
        output_path = Path(output_path)
        output_path.parent.mkdir(parents=True, exist_ok=True)
        output_path.write_text(code, encoding="utf-8")

    return code
